#include <bits/stdc++.h>
using namespace std;

int randInRange(int low, int high)
{
    return (rand()%(high-low))+(low+1) ;
}

int genPrime3mod4()
{
    while(true)
    {
        int num = randInRange(10000,100000); // to generate large random number
        bool prime = true;
        for(int i=2; i<=sqrt(num); i++)
        {
            if(num % i == 0)
            {
                prime = false;
                break;
            }
        }
        if(prime && num%4 == 3) return num;
    }
}

int bbs(int p, int q)
{
    long n = p*q ;

    long s; // non-zero and relatively prime to n
    do{ s = rand(); } while(s%p==0 || s%q==0 || s==0);

    int B = 0;
    unsigned long x = (s*s) % n;
    for(int i=0; i<10; i++) // to generate 10 bit output
    {
        x = (x*x) % n;
        B = B<<1 | (x & 1); // x%2 = x&1
    }

    if(B==0) return bbs(p,q); // check that the number generated should not be zero
    cout<<"Blum Blum Shub"<<endl<<"--------------"<<endl;
    cout<<"p = "<< p <<"\nq = "<< q <<"\nn = "<< n <<"\ns = "<< s <<endl;
    return B;
}

// (num pow p)%n
int powModN(int num,int p,int n)
{
	int res=1;
	for(int i=0; i<p; i++)
	{
        res = res * num;
        res = res %  n;
	}
	return res;
}

bool rabinMiller(int n)
{
    int q = n-1;
    int k = 0;
    while(q % 2 == 0)
    {
        q = q/2;
        k++ ;
    }

    cout << "\nRabin Miller(" << n << ")\n-----------------" << endl;
    cout << n-1 << " = 2^" << k << " * " << q << endl;
    cout << "k = " << k << "\nq = " << q;

    for(int i=0; i<4; i++) // check for 4 different values of a
    {
        bool flag = false;
        int a = randInRange(1, n-1);
        cout << "\na = " << a << endl;

        // if (a pow q)%n == 1
        if(powModN(a,q,n) == 1)
        {
            cout << "Inconclusive" << endl;
            continue;
        }
        for(int j=0; j<=k-1; j++)
        {
            if(powModN(a, pow(2,j)*q, n) == n-1)
            {
                flag = true;
                cout << "Inconclusive" << endl;
                break;
            }
        }
        if(flag == true) continue;
        else return false;
    }
    return true;

}

int main()
{
    srand(time(NULL));
    int p = genPrime3mod4(); // large prime number (p%4=3)
    int q = genPrime3mod4(); // large prime number (q%4=3)
    int randNum = bbs(p, q);
    cout << "Random number generated by BBS = " << randNum << endl;

    bool isPrime = true;
    isPrime = rabinMiller(randNum);
    if(!isPrime)  cout << "Composite" << endl << endl;
}

